    <p>We were lucky to find out through a friend that this house was up for sale before it hit the market. We were looking for a good amount of property to have garden, build a chicken coop, to be able to see stars, and that was far enough away from our neighbors that they wouldn't be disturbed from my need for loud music.</p>

    <p>For the first few months it felt like were on an extended vacation in an AirBNB. After settling into our new flow and hosting a few gatherings it finally started feeling like our space. So far we've only hung shelves and photos, but big changes will take place over time. We are in no rush!</p>

    <p>Phil and I are starting this blog to document our growth through the process of adding personal touches to our home and creating a tiny farm.</p>

