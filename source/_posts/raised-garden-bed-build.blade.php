    <h2>Materials</h2>
    <p>Building supplies</p>
    <ul>
        <li>Makita drill and impact driver with lithium-ion brushless motor. These things rock.</li>
        <li>A 1980's Craftsman circular saw</li>
        <li>16x 2in. x 12in. x 12ft ground contact boards</li>
        <li>6x 4in. x 4in. x 8ft ground contact posts</li>
    </ul>

    <p>Earth moving tools</p>
    <ul>
        <li>Truper contractor wheelbarrow</li>
        <li>D-handle aluminum scoop shovel for scooping lots of dirt</li>
        <li>Silage pitchfork for scooping lots of mulch</li>
        <li>Bow rake to spread the mulch and dirt around</li>
    </ul>

    <h2>Considerations</h2>
    <p>You may be asking yourself, "Why would they choose to use pressure treated ground contact boards? Won't that leach arsenic or other badness into the soil and plants? Why would they feed that food to their family?"</p>
    <ol>
        <li>We are not using CCA treated wood. https://www.epa.gov/ingredients-used-pesticide-products/chromated-arsenicals-cca</li>
        <li>https://extension.oregonstate.edu/gardening/techniques/raised-bed-lumber-pressure-treated-safe</li>
        <li>https://www.naturalhandyman.com/iip/infxtra/infptforraisedgardens.html</li>
        <li>I do not want to rebuild these garden beds in the next 10 years. I figured that if we spent money on higher quality materials rated for ground contact and the elements that we could extend the functional life of our raised garden beds. By my calculations, we need to harvest 468 heads of cauliflower until the project pays for itself.</li>
    </ol>

    <h2>Construction</h2>
    <p>The lumber order was placed early June 2018 from a local lumber supply warehouse. Delivery was arranged and a few days later a flatbed truck pulled into the driveway and began unloading. I messaged Jenny to prepare for <a href='/assets/images/2018-06-01-raised-bed-build/kirk-smirk.gif'>handling big wood</a> when she got home from work. Upon seeing the stack of wood, Jenny jumped up and down and exclaimed a simple, but heartwarming, "<a href='/assets/images/2018-06-01-raised-bed-build/yay.jpg'>Yay!</a>". We moved the lumber into the barn for another day. This would be both of our first times creating wood boxes and it was definitely fun, but hard work and took several hours to finish.</p>

    <p>To build the boxes, our plan was to utilize simple <a href='http://www.woodworkbasics.com/butt-joint.html'>butt joints</a> combined with corner posts for extra support. The corner posts were added once the boxes were moved into their final resting position. The box build went as follows:</p>
    <ol>
        <li>Using a large speed square, strike marks 3ft. apart on 4x of the 12ft. boards to make 4x short pieces. These 3ft. sections would be used as the short ends of each raised bed.</li>
        <li>Place the marked boards on sawhorses and cut them with a circular saw.</li>
        <li>Using a small speed square, strike marks 2ft. apart on each of the 6x 8ft posts. A single post will provide the corner reinforcement blocking for each bed.</li>
        <li>Our circular cannot cut to a depth of 4in. so we opted to use the miter saw instead.</li>
        <li>We arranged 2x 12ft. and 2x 3ft. lumber pieces into a rough shape, used the large speed square to square up one corner at a time, and screwed the corner together into a basic butt joint.</li>
        <li>As each box was assembled, we carried them out to the garden into a staging area.</li>
        <li></li>
    </ol>

    <h2>Moving the Earth</h2>
    <p>The spot we picked is larger than our previous house. This was crazy to actually mark out on the ground. We utilized aspects of <a href='https://richsoil.com/hugelkultur/'>hugelkultur</a> and <a href='https://healthysustainableliving.blogspot.com/2015/04/have-youtried-raised-lasagna-bed-garden.html'>lasagna gardening</a> while filling each bed.</p>

